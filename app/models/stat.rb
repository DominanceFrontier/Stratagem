class Stat < ActiveRecord::Base
    
  #----------------------------------------------------------------------------
  # Relationships
  #----------------------------------------------------------------------------
  belongs_to :player, polymorphic: true
  #----------------------------------------------------------------------------

  #----------------------------------------------------------------------------
  # Validations
  #----------------------------------------------------------------------------
  validates :player, presence: true
  #----------------------------------------------------------------------------
  
  def win_loss_ratio
    if self.wins == 0
      return 0.to_f
    elsif self.losses == 0
      return self.wins.to_f 
    else 
      return (self.wins / self.losses).to_f
    end 
  end 

end
